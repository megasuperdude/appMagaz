package com.example.islamdip;

public class MutuallyProduct {

    public String productId, productPrice, productTitle, productImage, productDescription, productSize, productColor, productMaterial, productCategory, productStatus, productCounter;

    public MutuallyProduct(String productId, String productPrice, String productTitle, String productImage, String productDescription, String productSize, String productColor, String productMaterial, String productCategory, String productStatus, String productCounter) {
        this.productId = productId;
        this.productPrice = productPrice;
        this.productTitle = productTitle;
        this.productImage = productImage;
        this.productDescription = productDescription;
        this.productSize = productSize;
        this.productColor = productColor;
        this.productMaterial = productMaterial;
        this.productCategory = productCategory;
        this.productStatus = productStatus;
        this.productCounter = productCounter;
    }

    public MutuallyProduct() {

    }
}
